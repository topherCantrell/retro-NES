printString:
; Print a null-terminated string to the screen
;
; GP0 = ROM address of string
; GP1 = Name table address
;
      lda    PPUSTATUS     ; Clear address vram address latch
      lda    GP1+1         ; MSB of ...
      sta    PPUADDR       ; ... name table address
      lda    GP1           ; LSB of ...
      sta    PPUADDR       ; ... name table address
      ldy    #0x00         ; First byte of string
_printLoop:
      lda    (GP0),Y       ; Next character
      beq    _printDone    ; End of string
      sta    PPUDATA       ; To screen
      iny                  ; Next character
      jmp    _printLoop    ; Do all
_printDone:
      rts                  ; Done

printHexByte:
; Print a 2 digit hex byte to the screen
;
; A = Value to print
; GP1 = Name table address
;
      tax                  ; Save value
      lda    PPUSTATUS     ; Clear address vram address latch
      lda    GP1+1         ; MSB of ...
      sta    PPUADDR       ; ... name table address
      lda    GP1           ; LSB of ...
      sta    PPUADDR       ; ... name table address
      txa                  ; Restore value
      lsr    a             ; High ...
      lsr    a             ; ...
      lsr    a             ; ...
      lsr    a             ; ... nibble      
      clc                  ; Clear carry
      adc    #0x30         ; Convert to ASCII
      cmp    #0x3A         ; Greater than 9?
      bcc    _printHex1    ; No
      clc
      adc    #7
_printHex1:
      sta    PPUDATA       ; To screen
      txa                  ; Restore value
      and    #0x0F         ; Low nibble
      clc                  ; Clear carry
      adc    #0x30         ; Convert to ASCII
      cmp    #0x3A         ; Greater than 9?
      bcc    _printHex2    ; No
      clc
      adc    #7
_printHex2:
      sta    PPUDATA       ; To screen      
      rts

